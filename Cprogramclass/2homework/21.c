#include <stdio.h>

int main()
{
    printf("*******************************************\n") ;
    printf("*      *                   *      *       *\n") ;
    printf("* (D)  *       (B)         * (O)  *   (H) *\n") ;
    printf("*      *                   *      *       *\n") ;
    printf("*******************************************\n") ;
    for ( int i = 1 ; i <= 256 ; i ++ )
    {
        printf("* %4d * ", i) ;

        //输出二进制等效数值
        for ( int b = 1 << 15 ; b ; b >>= 1 )
        {
            if ( b & i ) printf("1") ;
            else printf("0") ;
            
            if ( b == 1 << 8 ) printf(" ") ;
        }

        //输出八进制等效数值
        printf(" * %#4o" , i ) ;

        //输出十六进制等效数值
        printf(" * %#5x *\n" , i ) ;

        printf("*******************************************\n") ;
    }

    return 0 ;
}
